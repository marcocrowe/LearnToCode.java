package com.markcrowe;

public class ForLoopExample
{
	public static void main(String[] args)
	{
		System.out.println("A for loop example.");

		for(int counter = 1; counter <= 5; counter++)
		{
			System.out.println("Counter: " + counter);
		}
	}
}
